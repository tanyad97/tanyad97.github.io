                    <div id="dost"><h2>Достопримечательности Барселоны</h2></div>
                    <div class="content-text">
                        <div class="blog_item">
                            <div class="col-md-6">
								<p>Барселона – крупнейший исторический, культурный и торгово-промышленный город Испании, второй (после Мадрида)  по количеству жителей. Барселона ведет свою летопись с античных времен, множество памятников архитектуры и истории сохранилось здесь до наших времен. Именно по этой причине достопримечательности Барселоны - одни из важнейших пунктов всех туристических маршрутов Европы.
                                <p>На бульваре Ла Рамбла, в центре старого города, жизнь бьет ключом. Смешные уличные спектакли и концерты оживляют этот морской бульвар. Обязательно посетите Кафедральный Собор Барселоны, Поющие фонтаны Монтжуик, Гору Тибидабо и Парк Гуэля. А также музей Пикассо - один из нескольких музеев Барселоны, посвященных творчеству великого художника, который жил и творил здесь.</p>
                                <p>Самая известная из достопримечательностей Барселоны: Храм Святого Семейства - выглядит необычно, величественно и волшебно, как будто бы Храм соткан из тончайшего кружева или паутины. Вы будете очарованы «Испанской деревней» - музеем под открытым небом, архитектурным ансамблем, составленным из самых известных и красивейших зданий всех регионов Испании. Такие достопримечательности Барселоны, как  Стадион Камп Ноу и Башня Агбар относятся к произведениям современной архитектуры, но и они, несомненно, заслуживают внимания приезжающих.</p>
