                     <h3>Монтсеррат</h3>
                        <img src="img/monserrat.jpg" width="350px"  alt="alt">
                        <p>Всего лишь в 50 км от Барселоны, на вершине горы  в центре натурального парка, расположена одна из основных достопримечательностей Каталонии, ее святыня и гордость - монастырь Монтсеррат  (Monasterio Montserrat)..В настоящее время Монтсеррат, в первую очередь, известен как монастырь, где содержится покровительница Каталонии и главная ее духовная святыня - статуя Черной Мадонны, ей приписывают даже чудесные целительные свойства.</p>
                    </div>
                     <div class="col-md-4">
                     <h3>Гора Монтжуик</h3>
                       <img src="img/monjuic.jpg" width="350px"  alt="alt">
                        <p>Крепость Монтжуик служит своего рода символом Барселоны. Она находится на высоте 200 м над уровнем моря. Основана она была каталонцами во времена известной «Войны Жнецов» в 1640 г. Сегодня в ней располагается Военный музей, в котором представлены коллекции оружия и образцы воинского обмундирования различных мест и эпох. Также здесь можно увидеть макеты замков Каталонии, различные документы и предметы, связанные с ее историей.У подножья крепости находится смотровая площадка, с которой открывается великолепный панорамный вид на Барселону и ее порт.</p>
                    </div>
                     <div class="col-md-4">
                     <h3>Поющий фонтан</h3>
                        <img src="img/fontmagica.jpg" width="350px"  alt="alt">
                        <p>Поющие фонтаны (Font Màgica de Montjuïc), которые украшают Национальный дворец и парк Монтжуик огромны, их площадь превышает 3 000 кв. метров, а высота струй достигает 50 метров.Фонтан представляет собой множество маленьких фонтанчиков. Одни называют его поющим, другие танцующим, а кто-то просто – Волшебным. В общем-то, все перечисленное правда. Звуки классической и популярной музыки (отрывки из концертов Чайковского и Баха, оперы «Кармен», песен Монсеррат Кабалье и Фредди Меркьюри) заставляют водяные струи перемещаться в такт и беспрестанно менять форму фонтана, вода при помощи подсветки как будто бы окрашивается в разные цвета.</p>
                    </div>
                    <div class="col-md-4">
                     <h3>Памятник Христофору Колумбу</h3>
                       <img src="img/columbus.jpg" width="350px"  alt="alt">
                        <p>Колумб (Mirador de Colom) стоит на 60-метровой колонне и показывает пальцем в безмятежную и безбрежную даль моря. Этот памятник не случайно появился на маленькой площади вблизи от барселонского порта и Рамблы: первооткрыватель Америки приплыл именно сюда, чтобы в тронном зале Tinell сообщить королевской чете Изабелле и Фердинанду - радостную новость о найденном морском пути в Индию. Произошло это в 1492 году.</p>
                    </div>
                    <div class="col-md-4">
                     <h3>Дом Мила</h3>
                       <img src="img/casamla.jpg" width="350px"  alt="alt">
                        <p>За громоздкость фасада Дом Мила (La Casa Milà) в народе называют «каменоломней» (La Pedrera). На волнообразной поверхности этого здания не отыскать ни одной прямой линии. Фантазией Гауди двигало желание создать жилое пространство, залитое естественным светом.Кривые линии, волнообразные стены фасада, ломаные очертания тех или других элементов – все это похоже на подводный мир, волнистое песчаное дно, мягкие линии морского берега.</p>
                    </div>
                    <div class="col-md-4">
                     <h3>Гора Тибидабо</h3>
                        <img src="img/tibidabo.jpg" width="350px"  alt="alt">
                        <p>Символом Барселоны является 500-метровая вершина Тибидабо (Tibidabo) – это лучшая из городских смотровых площадок. Кроме того, это еще и довольно обширная территория, где разместился замечательный готический храм Святого Сердца (или Templе Expiatori del Sagrat Cor) с гигантской статуей Иисуса Христа на крыше, а также парк аттракционов (один из самых старых в мире) - Tibidabo Amusement Park.Лучше всего приезжать сюда сразу с утра, чтобы успеть прокатиться на всех серпантинах и горках, а оставшись до заката, полюбоваться лежащим внизу огромным городом в сиянии фонарей, окон и фар.</p>
