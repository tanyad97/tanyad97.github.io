                         <h2>Барселона</h2>
                         <p>Барселона (Barcelona) расположена в северо-восточной части Иберийского полуострова, на берегу Средиземного моря. Этот второй по величине город Испании является столицей Каталонии и самым крупным портом Средиземноморья. Барселона во всем мире известна как город искусств, однако, это еще и один из самых больших индустриальных и торговых городов страны. Кроме того, Барселона - это еще и крупнейший туристический центр Европы. Здесь удивительным образом сочетаются старинные здания, дворцы, храмы и ультрасовременные сооружения, а широкие улицы скрываются в тени платанов и экзотических пальм. Многие считают, что Барселона - это самый красивый и необыкновенный город в мире.</p>
